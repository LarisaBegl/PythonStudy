######################################################################################################################
print("\nЗадание 3\n")
import random
lenList = int(input("Введите размер списка:\n"))
A=[]
for i in range(lenList):
    a=random.randint(0,99)
    A.append(a)
print(A)
print("Ответ: максимальное значение списка - {}".format(max(A)))