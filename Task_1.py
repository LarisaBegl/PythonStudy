#Торговая фирма в первый день работы реализовала товаров на P тыс. руб., а затем ежедневно увеличивала выручку на 3%.
# Какой будет выручка фирмы в тот день, когда она впервые превысит заданное значение Q? Сколько дней придется торговать
#фирме для достижения этого результата?
######################################################################################################################
print("\nЗадание 2\n")
vyruchka_start = int(input("Введите стоимость товаров, которую реализовала фирма в первый день (тыс.руб.):\n"))
vyruchka_max = int(input("Введите максимальное значение выручки (тыс.руб.):\n"))
vyruchka =vyruchka_start
day=1
while vyruchka <= vyruchka_max:
    vyruchka = vyruchka*1.03
    day = day+1
print("Ответ: Выручка фирмы достигнет {} на {} день торгов".format(vyruchka,day))

